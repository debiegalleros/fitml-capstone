def _worst_fit_fpr(prefix):
    """Max fit-class FPR across every audited group table (body_type,
    height_band, size_band) -- a single consistent scope applied the same
    way to both before and after."""
    vals = []
    for group in ("body_type", "height_band", "size_band"):
        rows = AUDIT[f"[{prefix}] equalized-odds FPR by group — {group}"]
        vals.extend(_f(r["fpr_fit"]) for r in rows)
    return max(vals)


