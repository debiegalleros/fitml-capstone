    story.append(Spacer(1, 3.4 * inch))
    story.append(P(COVER["lines"][3], COVER_SUB))
    story.append(NextPageTemplate("Normal"))
    story.append(PageBreak())

    # -- table of contents
    story.append(P("Contents", H1))
    story.append(toc)
    story.append(PageBreak())

    # -- sections
    for section in SECTIONS:
        heading_text = f"{section['number']}&nbsp;&nbsp;{section['title']}"
        h = Paragraph(heading_text, H1)
        h._bookmarkName = f"sec{section['number']}"
        story.append(h)
        for block in section["blocks"]:
            kind = block[0]
            if kind == "p":
                story.append(P(block[1]))
            elif kind == "h":
                story.append(P(block[1], H2))
            elif kind == "h2":
                story.append(P(block[1], H2))
            elif kind == "b":
                for item in block[1]:
                    story.append(P(f"– {item}", BULLET))
            elif kind == "fig":
                story.extend(figure(block[1], block[2]))
            elif kind == "table1":
                header = ["model", "accuracy", "macro-F1", "F1 fit",
                          "F1 large", "F1 small", "train time (s)"]
                rows = [[r["model"], r["accuracy"], r["f1_macro"],
                        r["f1_fit"], r["f1_large"], r["f1_small"],
                        r["train_time_s"]] for r in COMPARISON]
                story.append(data_table(header, rows,
                                        [1.35 * inch, 0.72 * inch, 0.85 * inch,
                                         0.6 * inch, 0.7 * inch, 0.7 * inch,
                                         0.85 * inch]))
                story.append(Spacer(1, 4))
            elif kind == "table2":
                header = ["metric", "before (unweighted)", "after (class-weighted)"]
                story.append(data_table(header, TABLE2_ROWS,
                                        [2.6 * inch, 1.6 * inch, 1.6 * inch]))
                story.append(Spacer(1, 4))
            elif kind == "table3":
                story.append(data_table(block[1], block[2], block[3]))
                story.append(Spacer(1, 6))
        story.append(Spacer(1, 6))

    def _after_flowable(flowable):
        if hasattr(flowable, "_bookmarkName"):
            text = flowable.getPlainText()
            doc.canv.bookmarkPage(flowable._bookmarkName)
            doc.notify("TOCEntry", (0, text, doc.page, flowable._bookmarkName))

    doc.afterFlowable = _after_flowable
    doc.multiBuild(story)
    print(f"wrote {OUT_PATH}")


