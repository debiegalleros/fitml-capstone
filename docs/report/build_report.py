def read_audit_tables():
    """Minimal pipe-table reader scoped to the fixed '### heading' + GFM
    table structure of audit_tables.md (machine-generated by
    src/fairness_audit.py, 'do not edit') -- not a general markdown
    parser, just enough to read this one file's known shape."""
    path = os.path.join(ASSETS, "fairness", "audit_tables.md")
    with open(path) as f:
        text = f.read()
    tables, current, header, rows = {}, None, None, []
    for line in text.splitlines():
        if line.startswith("### "):
            if current and header:
                tables[current] = [dict(zip(header, r)) for r in rows]
            current, header, rows = line[4:].strip(), None, []
        elif line.startswith("|"):
            cells = [c.strip() for c in line.strip("|").split("|")]
            if all(set(c) <= {"-"} for c in cells):
                continue
            if header is None:
                header = cells
            else:
                rows.append(cells)
    if current and header:
        tables[current] = [dict(zip(header, r)) for r in rows]
    return tables


